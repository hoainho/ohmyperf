#!/usr/bin/env node
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..");
const brandsDir = resolve(pkgRoot, "brands");
const outDir = resolve(pkgRoot, "src/generated");
const outPath = resolve(outDir, "brand-css.ts");

const VENDORED_BRANDS = ["linear-app", "stripe", "vercel"];

await mkdir(outDir, { recursive: true });

const entries = [];
for (const id of VENDORED_BRANDS) {
  const tokens = await readFile(resolve(brandsDir, id, "tokens.css"), "utf8");
  const bridge = await readFile(resolve(brandsDir, id, "bridge.css"), "utf8");
  const combined = `${tokens}\n${bridge}`;
  entries.push({ id, css: combined });
}

const header = `// SPDX-License-Identifier: Apache-2.0
// AUTO-GENERATED by packages/design-tokens/scripts/emit-brand-css.mjs
// Do NOT edit by hand. Run \`pnpm --filter @ohmyperf/design-tokens build\` to refresh.

import type { BrandId } from "../brands.js";

`;

const escapeBackticks = (s) => s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");

const body = entries
  .map((e) => `export const ${tsConst(e.id)}_CSS: string = \`\n${escapeBackticks(e.css)}\n\`;`)
  .join("\n\n");

const map = `export const VENDORED_BRAND_CSS: Readonly<Record<Exclude<BrandId, "calibre">, string>> = {
${entries.map((e) => `  "${e.id}": ${tsConst(e.id)}_CSS,`).join("\n")}
};`;

function tsConst(brandId) {
  return brandId.toUpperCase().replace(/-/g, "_");
}

await writeFile(outPath, `${header}${body}\n\n${map}\n`, "utf8");
console.log(`emit-brand-css: wrote ${outPath} (${String(entries.length)} brands)`);
